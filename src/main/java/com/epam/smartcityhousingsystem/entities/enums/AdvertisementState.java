package com.epam.smartcityhousingsystem.entities.enums;

/**
 * This Enum is used to represent Advertisement state.
 * ON_SALE is for newly created advertisement and for advertisements on sale.
 * BOOKED is for advertisements that are being booked in order to do payment by a buyer
 * SOLD is for advertisements that are sold.
 *
 * @author Jakhongir Rasulov
 * @project smartcity-housing-system
 */
public enum AdvertisementState {
    ON_SALE, BOOKED, SOLD
}
